/*Objects*/
/*Key Value pair*/
let student ={
    name:"sudeep",
    age:21,
    course:"engineering"
};
console.log(student);
/*Accessing value with dot notation*/
console.log(student.name);
console.log(student.age);
console.log(student.course);

/*Example*/
let car = {
    brand: "Toyota",
    model: "Innova",
    year:2022
}
console.log(car.brand);
console.log(car.year);

let phone = {
    brand:"Samsung",
    model:"Galaxy Ultra",
    price:40000
}
console.log(phone.brand);
console.log(phone.price);